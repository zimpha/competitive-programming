int gcd(int a, int b) {
  return b ? gcd(b, a % b) : a;
}

class NonSimilarTriangles {
    public:
    int count(vector<int> L) {
      std::sort(L.begin(), L.end());
      int n = L.size();
      std::set<std::tuple<int, int, int>> s;
      for (int i = 0; i < n; ++i) {
        for (int j = i + 1; j < n; ++j) {
          for (int k = j + 1; k < n; ++k) if (L[i] + L[j] > L[k]) {
            int g = gcd(L[i], gcd(L[j], L[k]));
            s.emplace(L[i] / g, L[j] / g, L[k] / g);
          }
        }
      }
      return s.size();
    }
};

