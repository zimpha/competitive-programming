class NotTwo {
    public:
    int solve(int n, int m) {
        return n * m / 2 + n * m % 2;
    }
    int maxStones(int width, int height) {
        int n0 = width / 2 + width % 2, n1 = width - n0;
        int m0 = height / 2 + height % 2, m1 = height - m0;
        return solve(n0, m0) + solve(n1, m1) + solve(n0, m1) + solve(n1, m0);
    }
};

