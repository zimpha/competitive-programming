class CoinConstruction {
    public:
    vector<int> construct(int k) {
        int sum = 1000, first = 0;
        std::vector<int> res;
        for (int i = 19; i >= 0; --i) {
            if (k >> i & 1) {
                if (!first) {
                    first = 1;
                    res.push_back(sum);
                    sum = 0;
                } else {
                    res.push_back(sum + 1);
                }
                sum += res.back();
            } else if (first) {
                res.push_back(sum);
                sum += res.back();
            }
        }
        return res;
    }
};

