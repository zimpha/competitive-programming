class ModModMod {
    public:
    long long findSum(vector<int> m, int R) {
      std::map<int, int> mp;
      mp[R] = 1;
      for (auto &&mod: m) {
        auto it = mp.lower_bound(mod);
        for (; it != mp.end(); ) {
          int x = it->first / mod;
          int y = it->first % mod;
          mp[mod - 1] += x * it->second;
          mp[y] += it->second;
          it = mp.erase(it);
        }
      }
      long long ret = 0;
      for (auto &&e: mp) {
        ret += 1ll * e.first * (e.first + 1) / 2 * e.second;
      }
      return ret;
    }
};

